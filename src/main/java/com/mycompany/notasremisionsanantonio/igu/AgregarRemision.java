    private void cargarClientesEnComboBox() {
        ClienteDAO clienteDAO = new ClienteDAO();
        List<Cliente> clientes = clienteDAO.obtenerClientes();
        for (Cliente cliente : clientes) {
            if (cliente.isEstatus()) {
                jComboBox1.addItem(cliente); 
            }
        }
    }

